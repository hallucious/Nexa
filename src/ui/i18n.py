def normalize_ui_language(app_language: str | None) -> str:
    if not app_language:
        return DEFAULT_UI_LANGUAGE
    value = str(app_language).strip().lower().replace('_', '-')
    if value.startswith('ko'):
        return 'ko'
    return 'en'


def ui_language_from_sources(*sources: Any) -> str:
    for source in sources:
        if source is None:
            continue
        parsed_model = getattr(source, 'parsed_model', None)
        if parsed_model is not None:
            source = parsed_model
        ui = getattr(source, 'ui', None)
        metadata = getattr(ui, 'metadata', None)
        if isinstance(metadata, Mapping):
            for key in ('app_language', 'locale', 'language'):
                if metadata.get(key):
                    return normalize_ui_language(str(metadata.get(key)))
    return DEFAULT_UI_LANGUAGE


def make_display_text_ref(text_key: str, *, fallback_text: str | None = None, params: Mapping[str, Any] | None = None) -> DisplayTextRef:
    return DisplayTextRef(text_key=text_key, fallback_text=fallback_text, params=dict(params or {}))


def resolve_display_text(ref: DisplayTextRef, *, app_language: str | None = None, fallback_language: str = DEFAULT_UI_LANGUAGE) -> str:
    language = normalize_ui_language(app_language)
    fallback = normalize_ui_language(fallback_language)
    template = (
        _TRANSLATIONS.get(language, {}).get(ref.text_key)
        or _TRANSLATIONS.get(fallback, {}).get(ref.text_key)
        or ref.fallback_text
        or ref.text_key
    )
    if ref.params:
        return template.format(**ref.params)
    return template


def ui_text(text_key: str, *, app_language: str | None = None, fallback_text: str | None = None, params: Mapping[str, Any] | None = None, **kwargs: Any) -> str:
    merged_params = dict(params or {})
    merged_params.update(kwargs)
    ref = make_display_text_ref(text_key, fallback_text=fallback_text, params=merged_params)
    return resolve_display_text(ref, app_language=app_language)


def _unwrap_beginner_source(source: Any) -> Any:
    if isinstance(source, LoadedNexArtifact):
        return source.parsed_model
    return source


def _beginner_metadata_from_source(source: Any) -> Mapping[str, Any]:
    source = _unwrap_beginner_source(source)
    if isinstance(source, WorkingSaveModel) and isinstance(getattr(source.ui, 'metadata', None), Mapping):
        return source.ui.metadata
    return {}


def _beginner_first_success_achieved(*sources: Any) -> bool:
    for source in sources:
        source = _unwrap_beginner_source(source)
        if isinstance(source, ExecutionRecordModel) and source.meta.status == 'completed':
            return True
        metadata = _beginner_metadata_from_source(source)
        if bool(metadata.get('beginner_first_success_achieved')):
            return True
        if isinstance(source, WorkingSaveModel):
            last_run = getattr(source.runtime, 'last_run', None) or {}
            status = str(last_run.get('status') or last_run.get('semantic_status') or '').lower()
            if status == 'completed':
                return True
    return False


def beginner_language_enabled(*sources: Any) -> bool:
    has_working_save = False
    for source in sources:
        source = _unwrap_beginner_source(source)
        if isinstance(source, WorkingSaveModel):
            has_working_save = True
            metadata = _beginner_metadata_from_source(source)
            if bool(metadata.get('advanced_mode_requested')):
                return False
            if str(metadata.get('user_mode') or '').lower() == 'advanced':
                return False
    if not has_working_save:
        return False
    return not _beginner_first_success_achieved(*sources)


def beginner_ui_text(text_key: str, *, beginner_text_key: str | None = None, sources: tuple[Any, ...] = (), app_language: str | None = None, fallback_text: str | None = None, params: Mapping[str, Any] | None = None, **kwargs: Any) -> str:
    resolved_key = beginner_text_key if beginner_text_key and beginner_language_enabled(*sources) else text_key
    return ui_text(resolved_key, app_language=app_language, fallback_text=fallback_text, params=params, **kwargs)


